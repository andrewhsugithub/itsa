#include <stdio.h>
#include <string.h>

int main()
{
    char *digits[10][5] = {{"*****", "*   *", "*   *", "*   *", "*****"},
                           {"    *", "    *", "    *", "    *", "    *"},
                           {"*****", "    *", "*****", "*    ", "*****"},
                           {"*****", "    *", "*****", "    *", "*****"},
                           {"*   *", "*   *", "*****", "    *", "    *"},
                           {"*****", "*    ", "*****", "    *", "*****"},
                           {"*****", "*    ", "*****", "*   *", "*****"},
                           {"*****", "    *", "    *", "    *", "    *"},
                           {"*****", "*   *", "*****", "*   *", "*****"},
                           {"*****", "*   *", "*****", "    *", "    *"}};
    char a, b, c, d;
    scanf("%c%c%c%c", &a, &b, &c, &d);

    for (int i = 0; i < 5; i++)
    {
        printf("%s ", digits[a - '0'][i]);
        printf("%s ", digits[b - '0'][i]);
        printf("%s ", digits[c - '0'][i]);
        printf("%s\n", digits[d - '0'][i]);
    }
}